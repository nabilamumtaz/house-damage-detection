# Path dataset split
train_path = "splitted_dataset/train"
val_path = "splitted_dataset/val"
test_path = "splitted_dataset/test"

# Data Augmentation
datagen = ImageDataGenerator(
    rescale=1.0/255,
    rotation_range=30,
    width_shift_range=0.2,
    height_shift_range=0.2,
    shear_range=0.2,
    zoom_range=0.2,
    horizontal_flip=True,
    fill_mode="nearest"
)

test_datagen = ImageDataGenerator(rescale=1.0/255)

# Data Generator
train_gen = datagen.flow_from_directory(
    directory=train_path,
    target_size=(128, 128),
    batch_size=32,
    class_mode="categorical"
)

val_gen = datagen.flow_from_directory(
    directory=val_path,
    target_size=(128, 128),
    batch_size=32,
    class_mode="categorical"
)

test_gen = test_datagen.flow_from_directory(
    directory=test_path,
    target_size=(128, 128),
    batch_size=32,
    class_mode="categorical",
    shuffle=False
)

"""# <a id='splitting' href=#splitting>
<h1 style="font-family: Garamond; font-size: 40px; font-style: normal; letter-spacing: 3px;
background-color: #f6f5f5; color: #003f88; border-radius: 100px 100px; text-align: center;">
Model Training ğŸš€
</h1>
</a>

"""

# Model MobileNetV2
base_model = MobileNetV2(weights="imagenet", include_top=False, input_shape=(128, 128, 3))
base_model.trainable = False

model = Sequential([
    base_model,
    GlobalAveragePooling2D(),
    Dropout(0.5),
    Dense(128, activation='relu'),
    Dropout(0.3),
    Dense(train_gen.num_classes, activation='softmax')
])

model.compile(optimizer='adam',
              loss='categorical_crossentropy',
              metrics=['accuracy'])

# Pelatihan Model
history = model.fit(train_gen, validation_data=val_gen, epochs=25)

# Visualisasi Train Loss
plt.plot(history.history['loss'], label='Train Loss')
plt.plot(history.history['val_loss'], label='Val Loss')
plt.legend()
plt.title("Train vs Val Loss")
plt.show()

#Visualisasi Akurasi
plt.plot(history.history['accuracy'], label='Train Accuracy')
plt.plot(history.history['val_accuracy'], label='Val Accuracy')
plt.legend()
plt.title("Train vs Val Accuracy")
plt.show()

